        int rVal = c.showOpenDialog(null);
        if (rVal == JFileChooser.APPROVE_OPTION) {
        String filename = c.getSelectedFile().getName();
        String dir = c.getCurrentDirectory().toString();
        filePath = dir+"\\"+filename;
        txtImport.setText(filePath);
        }
